Vector1 = [(1,2,-3,4)]
a = []

for numbers in Vector1:
    for number in numbers:
        square1 = number **2
        a.append(square1)

print('Given:                       ',Vector1)
print('The square of all numbers    ',a)
print('The sum of all numbers:      ',sum(a))
print('The square root of the sum:  ',sum(a)**(1/2))
print('#'*64)
